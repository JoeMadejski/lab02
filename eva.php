            <h3>Complementary Ending</h3>

            <p>The complementary ending to Neon Genesis Evangelion is first teased in Rebirth, 
            the second half of the theatrical film Neon Genesis Evangelion: Death & Rebirth. 
            Rebirth comprises the first twenty-five minutes of Episode 25' and ends as the 
            Mass Production Evangelions under the control of SEELE sortie to combat Asuka 
            under command of NERV. The full ending, split into two 45-minute long episodes, 
            25' and 26', is shown in the theatrical film The End of Evangelion.</p>
            
