                <h2>Episodes</h2>
                
                <p> Each episode has two titles: one is the original Japanese title, and the second 
                is an English title that was chosen by Japanese studio Gainax itself and appears 
                as an eye catch. Most often, the official English title is not a direct translation 
                of the Japanese title. For example, the direct translation of the Japanese title of 
                episode 2 is "Unfamiliar Ceilings", but the English title is "The Beast". Sometimes, 
                however, the two titles are either similar or exactly the same, as was the case with 
                the first episode "Angel Attack".</p>

